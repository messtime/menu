          <div class="title">卤水的使用及保管方法</div>
          <ol>
            <li>卤水的使用</li>
            <ol>
              <li>凡动物性原料在卤制前均需先做氽水处理，否则原料直接下锅后，会导致卤水急剧减少，从而造成菜品口味过咸。</li>
              <li>一锅上好的卤水，应经常卤制鲜味较浓的动物性原料，这样才能增加卤水的鲜香味。有一句行话叫做“卤水越老越好”，讲的就是这个道理。</li>
              <li>猪肉和鸡、鸭、鹅、兔这类鲜香味较浓的原料，应与异味较重的牛、羊肉及各种动物“下水”如肥肠原料分开使用卤水，以保证卤水和卤制菜品的质量。</li>
              <li>在使用过程中，要经常检查卤水的色泽、香味、咸度以及汤汁是否充足等，一旦发现某方面有所减少应及时补上，即我们常说的“缺啥补啥”。</li>
            </ol>
            <li>卤水的保管</li>
            <ol>
              <li>卤水经过一段时间的使用后，会留下少数原料或香料的残渣，这时便需要进行过滤，以此来保证卤水的质量。</li>
              <li>
                卤水经反复使用后汤汁会变得比较浓稠，虽经过滤，但还需“清扫”，即用干净的动物血液与清水混合后，徐徐加入到烧沸的卤水中，这便是利用蛋白质的吸附和凝固作用，吸去卤水中的杂质，以使卤水变得清澈，讲究一些的还要用瘦肉茸对卤水进行“清扫”。但需注意，每锅卤水清扫的次数不能过多，以免卤水失去鲜香味。
              </li>
              <li>卤水中浮油要经常打掉，最好使卤水表面只保留薄薄的一层“油面子”。否则，油脂过多，容易使卤水变质?脂肪氧化变质所致。</li>
              <li>
                卤水在不使用时，应烧沸后放入搪瓷桶内，令其自然冷却，且不要随意晃动。另外，桶底还应垫上砖块，以保持底部通风。若是夏天，卤水必须每天烧沸，如果有条件，还可放入冷库中存放。卤水在长期不用时，也应时常从冷库中取出烧沸，冷却后再放入库中。以上仅是笔者制作川式卤水的一点经验和体会。但烹饪是变化之学，所以读者在调制川式卤水时，还应根据具体情况灵活地掌握卤水的用料及调制方法，这样才能调制出令人满意的卤水来。
              </li>
