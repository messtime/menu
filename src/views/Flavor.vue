          <div class="title">川式卤水</div>
          <ol>
            <li>配方<br>八角25克 桂皮15克 小茴15～25克甘草10克三奈10克 甘菘3～5克 花椒20克 砂仁10克 草豆蔻5克草果15克 丁香5～15克 生姜100克 大葱150克绍酒100克冰糖350～500克
              味精15克 精盐350～500克 鲜汤5000克精炼油50克 纱布袋2个
            </li>
            <li>调制</li>
            <ol>
              <li>将八角、桂皮、小茴、甘草、三奈、甘菘、花椒、砂仁、草豆蔻、草果、丁香等分成两份，分别装入宽松的纱布袋中并用细绳扎紧袋口；姜洗净拍破；葱连根须洗净挽结。</li>
              <li>将大块的冰糖先在火上炙烤一下，然后放在菜板上轻轻敲碎，再与精炼油一同入锅，用小火炒至呈深红色时，掺入500克沸水搅匀，即成糖色。</li>
              <li>锅置火上，掺入鲜汤5000克，放入姜葱，调入精盐、味精和糖色，再放入香料包，烧沸后改用小火慢慢地熬至香味四溢时，即成新鲜卤水。</li>
            </ol>
            <li>需要注意的问题</li>
            <ol>
              <li>炒糖色时，必须用小火慢炒，且糖色应稍嫩一些，否则炒出的糖色有苦味。</li>
              <li>
                按传统方法调制的卤水通常都不加味精，但由于新鲜卤水大都鲜味不足，加之近年来人们对鲜味的要求似乎已越来越高，所以在调制的过程中也可适量加入的味精。需要说明的是，在卤水中加入味精并不会起副作用，因为味精在160℃以上的温度中才会产生焦谷酸钠从而失去鲜味，而卤水沸腾时的温度一般不会超过
                105℃。</li>
              <li>卤水中一般应加入嫩糖色，如此才会使卤水有回甜味。而加入了嫩糖色以后，便可以不再加甘草。但从药物性能角度看，甘草有调和诸味及提鲜的作用。因此，在加了糖色以后，卤水中仍可考虑加少许甘草。</li>
              <li>丁香中含有丁香油酚，其味甚浓，使用时可根据具体情况调整用量。一般来说，5000克鲜汤中丁香用量应控制在5～15克之间。</li>
              <li>用于制作卤水的大葱应保留其根须，那样可使卤水的味道更香。这可是一位多年制作卤水的老师傅传授给笔者的经验。</li>
              <li>上述卤水配方中加有糖色，且色呈棕红，称为红卤，若去掉配方中的糖色便成了白卤。另外有人爱在卤水中加入干辣椒，那样就变成辣卤了。</li>
            </ol>
          </ol>

